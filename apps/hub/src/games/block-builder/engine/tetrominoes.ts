export const PIECE_KINDS: PieceKind[] = ["I", "O", "T", "S", "Z", "J", "L"];

export function pieceCells(piece: ActivePiece): Cell[] {
  const shape = SHAPES[piece.kind][piece.rotation % 4];
  return shape.map((c) => ({ x: c.x + piece.x, y: c.y + piece.y }));
}

export function rotatePiece(piece: ActivePiece, dir: 1 | -1): ActivePiece {
  return { ...piece, rotation: (piece.rotation + dir + 4) % 4 };
}

export function randomPieceKind(): PieceKind {
  return PIECE_KINDS[Math.floor(Math.random() * PIECE_KINDS.length)];
}

export function spawnPiece(kind: PieceKind): ActivePiece {
  return { kind, rotation: 0, x: 3, y: 0 };
}

export function previewCells(kind: PieceKind, rotation = 0): Cell[] {
  return SHAPES[kind][rotation % 4];
}
